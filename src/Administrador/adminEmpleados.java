    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser jf = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Imagenes", "jpg", "png", "jpeg"); 
        jf.setFileFilter(filtro);
        jf.showOpenDialog(null);
        File file = jf.getSelectedFile();
        if (file != null) {
            String origen = file.getPath();
            ImageIcon icon = new ImageIcon(origen);
            Image img = icon.getImage().getScaledInstance(txtImg.getWidth(), txtImg.getHeight(), Image.SCALE_SMOOTH);
            Icon icono = new ImageIcon(img);
            txtImg.setIcon(icono);
        }else{
            JOptionPane.showMessageDialog(null, "No se ha seleccionado una imagen", "Imagen no seleccionada", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

