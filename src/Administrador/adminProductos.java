    public void ganancias() {
        double costo = Double.parseDouble(Costo.getText());
        double porcentaje = Double.parseDouble(Porcentaje.getText());
        double porGan;
        porGan = costo * porcentaje /100;
        precioUni.setText(String.valueOf(porGan + costo));
        precioUni.setEnabled(true);
    }

